#include <unistd.h>

void	ft_is_negative(int n)
{
	int	a;
	int	b;

	a = 'P';
	b = 'N';
	if (n >= 0)
	{
		write(1, &a, 1);
	}
	else
	{
		write(1, &b, 1);
	}
}
